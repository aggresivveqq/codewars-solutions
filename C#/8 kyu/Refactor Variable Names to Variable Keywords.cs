{
    public bool @bool = false;       // bool
    public char @char = '\0';        // char
    public sbyte @sbyte = 0;         // sbyte
    public byte @byte = 0;           // byte
    public short @short = 0;         // short
    public ushort @ushort = 0;       // ushort
    public int @int = 0;             // int
    public uint @uint = 0;           // uint
    public long @long = 0;           // long
    public ulong @ulong = 0;         // ulong
    public float @float = 0;         // float
    public double @double = 0;       // double
    public decimal @decimal = 0;     // d  

}